package week1.day2;

public class Calculator {
	
	public int addNumbers(int a, int b, int c)
	{
		return a+b+c;
	}
	public int subNumbers (int x, int y) {
		return x-y;
	}
	public double multiplication(double n1, double n2) {
		return n1*n2;
	}
	public float division(float num1, float num2) {
		return num1/num2;
	}

}
